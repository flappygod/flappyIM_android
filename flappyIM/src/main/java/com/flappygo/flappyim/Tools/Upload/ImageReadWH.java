package com.flappygo.flappyim.Tools.Upload;

/******
 * 图片宽高
 */
public class ImageReadWH {


    //宽度
    private int width;

    //高度
    private int height;

    public ImageReadWH(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

}
